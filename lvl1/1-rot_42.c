#include <unistd.h>

int	main(int argc, char **argv)
{
	int i;

	i = 0;
	while (argc == 2 && argv[1][i] != '\0')
	{
		if ((argv[1][i] >= 'a' && argv[1][i] <= 'j') ||
				(argv[1][i] >= 'A' && argv[1][i] <= 'J'))
			argv[1][i] = argv[1][i] + 16;
		else if ((argv[1][i] >= 'k' && argv[1][i] <= 'z') ||
				(argv[1][i] >= 'K' && argv[1][i] <= 'Z'))
			argv[1][i] = argv[1][i] - 10;
		write(1, &argv[1][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
